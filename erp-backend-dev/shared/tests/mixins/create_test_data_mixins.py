class SharedTestDataMixin(MaterialTestDataMixin):

    def update_object_kwargs(self, object, **kwargs):
        '''
            This will update any parameters given. Example: if "name" field is given for a Customer object,
            this function will update the name field generated by model baker.
        '''
        for key, value in kwargs.items():
            setattr(object, key, value)
        object.save()

