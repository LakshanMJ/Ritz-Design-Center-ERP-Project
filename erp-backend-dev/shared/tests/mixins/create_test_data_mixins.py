    def create_order_inquiry(self, **kwargs):
        customer = kwargs.get('customer', None)
        if not customer:
            customer = self.create_customer()
        brand = kwargs.get('brand', None)
        if not brand:
            brand = self.create_brand()
        customer_brand = self.get_or_create_customer_brand(customer, brand)

        data = {
            'customer': customer,
            'brand': brand,
            'season': self.create_season(customer_brand=customer_brand, name='Fall'),
            'year': 2024,
            'pack_type': 'single',
            'costing_method': 'common_price',
            'size_category': self.create_size_category(),
            'number_of_colorways': 2,
            'quantity_per_pack': 1,
            'style_number': 'abcdefg',
            'merchant': self.create_user(),
        }
        data = {**data, **kwargs}
        order_inquiry = baker.make('marketing.OrderInquiry', **data)
        return order_inquiry

